    private void botonInfoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonInfoActionPerformed
        // TODO add your handling code here:
            botonInfo.addActionListener(new ActionListener() {
            int i = 0;
            String text=("\nHas ganado este combate"
                    + "\nSi continuas al sigueinte combate y no gana, perderas puntos"
                    + "\nÂ¿Quieres continuar?");
                    
            @Override
            public void actionPerformed(ActionEvent e) {
                Timer timer = new Timer(25, new ActionListener() {
                    @Override
                    public void actionPerformed(ActionEvent e) {
                        char character[] = text.toCharArray();
                        int arrayNumber = character.length;

                        String addedCharacter = "";
                        String blank = "";

                        if (i < arrayNumber) {
                            addedCharacter = blank + character[i];
                            areaInfo.append(addedCharacter);
                        }
                        i++;
                    }
                });
                timer.start();
            }
        });  
    }//GEN-LAST:event_botonInfoActionPerformed

